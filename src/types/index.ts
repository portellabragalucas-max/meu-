/**
 * Nexora Type Definitions
 * Central type definitions for the study optimization platform
 */

// ============================================
// User Types
// ============================================

export interface User {
  id: string;
  email: string;
  name: string;
  avatar?: string;
  
  // Gamification
  xp: number;
  level: number;
  streak: number;
  longestStreak: number;
  lastStudyDate?: Date;
  
  // Preferences
  dailyGoalHours: number;
  preferredStart: string;
  preferredEnd: string;
  maxBlockMinutes: number;
  breakMinutes: number;
  
  // AI Settings
  aiDifficulty: AIDifficulty;
  focusMode: boolean;
  
  createdAt: Date;
  updatedAt: Date;
}

export type AIDifficulty = 'easy' | 'medium' | 'hard' | 'adaptive';

// ============================================
// Subject Types
// ============================================

export interface Subject {
  id: string;
  userId: string;
  name: string;
  color: string;
  icon: string;
  studyPrefs?: StudyPreferences;
  area?: string;
  nivel?: string;
  pesoNoExame?: number;
  prerequisitos?: string[];
  tipo?: string;
  
  // Study parameters
  priority: number;      // 1-10
  difficulty: number;    // 1-10
  targetHours: number;   // Weekly target
  completedHours: number;
  
  // Progress
  totalHours: number;
  sessionsCount: number;
  averageScore: number;
  
  isActive: boolean;
  createdAt: Date;
  updatedAt: Date;
}

export interface StudyPreferences {
  hoursPerDay: number;
  daysOfWeek: number[];
  mode: 'random' | 'exam';
  examDate?: string;
  goal?: 'enem' | 'medicina' | 'concurso' | 'outros';
}

// ============================================
// Study Block Types
// ============================================

export type StudyBlockType =
  | 'AULA'
  | 'EXERCICIOS'
  | 'REVISAO'
  | 'SIMULADO_AREA'
  | 'SIMULADO_COMPLETO'
  | 'ANALISE';

export interface StudyBlock {
  id: string;
  userId: string;
  subjectId: string;
  subject?: Subject;
  
  // Timing
  date: Date;
  startTime: string;
  endTime: string;
  durationMinutes: number;
  
  // Roadmap metadata
  sessionType?: 'teoria' | 'pratica' | 'revisao' | 'simulado';
  phase?: string;
  area?: string;
  type?: StudyBlockType;
  description?: string;
  sequenceIndex?: number;
  relatedSubjectId?: string;
  
  // Status
  status: BlockStatus;
  isBreak: boolean;
  isAutoGenerated: boolean;
  
  createdAt: Date;
  updatedAt: Date;
}

export type BlockStatus = 'scheduled' | 'in-progress' | 'completed' | 'skipped';

// ============================================
// Study Session Types
// ============================================

export interface StudySession {
  id: string;
  userId: string;
  subjectId: string;
  blockId?: string;
  subject?: Subject;
  
  // Timing
  startedAt: Date;
  endedAt?: Date;
  plannedMinutes: number;
  actualMinutes: number;
  
  // Performance
  focusScore: number;      // 0-100
  productivityScore: number; // 0-100
  notes?: string;
  xpEarned: number;
  
  createdAt: Date;
}

// ============================================
// Achievement Types
// ============================================

export interface Achievement {
  id: string;
  name: string;
  description: string;
  icon: string;
  xpReward: number;
  condition: AchievementCondition;
  rarity: AchievementRarity;
}

export interface AchievementCondition {
  type: 'streak' | 'hours' | 'sessions' | 'level' | 'subject_complete';
  value: number;
}

export type AchievementRarity = 'common' | 'rare' | 'epic' | 'legendary';

export interface UserAchievement {
  id: string;
  userId: string;
  achievementId: string;
  achievement: Achievement;
  unlockedAt: Date;
}

// ============================================
// Analytics Types
// ============================================

export interface WeeklyStats {
  id: string;
  userId: string;
  weekStart: Date;
  
  totalHours: number;
  sessionsCount: number;
  avgFocusScore: number;
  avgProductivity: number;
  goalsCompleted: number;
  xpEarned: number;
  
  dailyBreakdown: DailyBreakdown[];
}

export interface DailyBreakdown {
  date: string;
  hours: number;
  sessions: number;
  focusScore: number;
}

export interface HeatmapData {
  date: string;
  value: number; // hours studied
  level: 0 | 1 | 2 | 3 | 4; // intensity level
}

export interface AnalyticsStore {
  daily: Record<string, { hours: number; sessions: number }>;
}

// ============================================
